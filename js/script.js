let current = 0;
let score = 0;
let time = 80 * 60; // 80 minutes

function loadQuestion() {
    if (current >= quiz.length) {
        finishQuiz();
        return;
    }

    document.getElementById("question").innerHTML =
        "Q" + (current + 1) + ". " + quiz[current].question;

    let html = "";

    quiz[current].options.forEach((option, index) => {
        html += `
        <label>
            <input type="radio" name="answer" value="${index}">
            ${option}
        </label><br><br>
        `;
    });

    document.getElementById("options").innerHTML = html;

    // Show section
    if (document.getElementById("section")) {
        document.getElementById("section").innerHTML =
            "<b>Section:</b> " + quiz[current].section;
    }
}

function skipQuestion() {
    current++;

    if (current >= quiz.length) {
        finishQuiz();
        return;
    }

    loadQuestion();
}

function nextQuestion() {
    const selected = document.querySelector(
        'input[name="answer"]:checked'
    );

    if (!selected) {
        alert("Please select an option.");
        return;
    }

    if (parseInt(selected.value) === quiz[current].answer) {
        score++;
    }

    current++;
    loadQuestion();
}

function finishQuiz() {
    let percentage = ((score / quiz.length) * 100).toFixed(2);

    document.querySelector(".login-box").innerHTML = `
        <h2>🎉 Quiz Completed</h2>
        <h3>Total Questions : ${quiz.length}</h3>
        <h3>Correct Answers : ${score}</h3>
        <h3>Wrong Answers : ${quiz.length - score}</h3>
        <h3>Percentage : ${percentage}%</h3>
    `;
}

function startTimer() {
    const timer = setInterval(() => {

        let minutes = Math.floor(time / 60);
        let seconds = time % 60;

        document.getElementById("timer").innerHTML =
            "Time Left : " +
            minutes +
            ":" +
            (seconds < 10 ? "0" : "") +
            seconds;

        time--;

        if (time < 0) {
            clearInterval(timer);
            finishQuiz();
        }

    }, 1000);
}

loadQuestion();
startTimer();